package com.codekiller.fcsaadmin.Datas;

public class AboutData {
    String name, subject, about_them, download_url, push_key;

    public AboutData() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getAbout_them() {
        return about_them;
    }

    public void setAbout_them(String about_them) {
        this.about_them = about_them;
    }

    public String getDownload_url() {
        return download_url;
    }

    public void setDownload_url(String download_url) {
        this.download_url = download_url;
    }

    public String getPush_key() {
        return push_key;
    }

    public void setPush_key(String push_key) {
        this.push_key = push_key;
    }
}
